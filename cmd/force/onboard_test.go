func fileMentionsSelector(t *testing.T, path, selector string) bool {
	t.Helper()
	src, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read %s: %v", path, err)
	}
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, path, src, parser.ParseComments)
	if err != nil {
		t.Fatalf("parse %s: %v", path, err)
	}
	found := false
	ast.Inspect(f, func(n ast.Node) bool {
		sel, ok := n.(*ast.SelectorExpr)
		if !ok {
			return true
		}
		if sel.Sel != nil && sel.Sel.Name == selector {
			found = true
			return false
		}
		return true
	})
	return found
}

func repoRootForTest(t *testing.T) string {
	t.Helper()
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		t.Fatalf("runtime.Caller failed")
	}
	// onboard_test.go is at <root>/cmd/force/onboard_test.go
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}

func newTempGitRepo(t *testing.T, readmeBody string) string {
	t.Helper()
	dir := t.TempDir()
	mustRunGitForOnboardTest(t, dir, "init", "-q")
	mustRunGitForOnboardTest(t, dir, "config", "user.email", "test@example.com")
	mustRunGitForOnboardTest(t, dir, "config", "user.name", "Test")
	mustRunGitForOnboardTest(t, dir, "config", "commit.gpgsign", "false")
	if err := os.WriteFile(filepath.Join(dir, "README.md"), []byte(readmeBody), 0644); err != nil {
		t.Fatalf("write README.md: %v", err)
	}
	mustRunGitForOnboardTest(t, dir, "add", "README.md")
	mustRunGitForOnboardTest(t, dir, "commit", "-q", "-m", "seed")
	return dir
}

func mustRunGitForOnboardTest(t *testing.T, dir string, args ...string) {
	t.Helper()
	cmd := exec.Command("git", append([]string{"-C", dir}, args...)...)
	cmd.Env = append(os.Environ(),
		"GIT_AUTHOR_NAME=t", "GIT_AUTHOR_EMAIL=t@x",
		"GIT_COMMITTER_NAME=t", "GIT_COMMITTER_EMAIL=t@x",
	)
	out, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("git %s: %v — %s", strings.Join(args, " "), err, strings.TrimSpace(string(out)))
	}
}

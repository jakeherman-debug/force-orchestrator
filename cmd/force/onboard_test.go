// onboard_test.go — D6 unit tests for the `force onboard` CLI.
//
// Coverage:
//
//   - TestOnboardingSynthesizesFromSenatorPipeline (exit criterion #4)
//     — the CLI invokes the same internal function the SenatorOnboarding
//       task uses. We assert this two ways, belt-and-suspenders:
//         (a) AST walk over inprocess_d4.go (the SenatorOnboarding
//             implementation) AND cmd/force/onboard.go: both must
//             reference the BuildRepoDigest selector.
//         (b) Behavioural — runOnboarding, given a mock librarian.Client,
//             must call MockClient.BuildRepoDigest exactly once.
//
//   - TestOnboardRendersSixRequiredSections (exit criterion #2) —
//     a rendered ONBOARDING.md has the 6 mandated section headings and
//     the AUTO-GENERATED header.
//
//   - TestOnboardWritesToRepoRoot — file lands at <local-path>/ONBOARDING.md.
//
//   - TestOnboardOnUnregisteredPath — disk-only repo path resolution works.
//
//   - TestOnboardRefreshFlagIsAlias — --refresh produces the same content
//     as a bare re-run (idempotence).
//
//   - TestOnboardHelpListsSubcommand (exit criterion #1) — `force onboard
//     --help` produces non-empty output mentioning the subcommand.

